export function getCategoryScores(
  answers: Record<number, string>
): CategoryScore[] {
  return QUIZ_CATEGORIES.map((cat) => {
    let score = 0;
    for (const qId of cat.questionIds) {
      const selectedOptionId = answers[qId];
      if (selectedOptionId) {
        const question = QUIZ_QUESTIONS.find((q) => q.id === qId);
        const option = question?.options.find(
          (o) => o.id === selectedOptionId
        );
        if (option) score += option.score;
      }
    }

    const percentage = Math.round((score / cat.maxScore) * 100);

    let label: string;
    let color: string;
    if (percentage >= 87.5) {
      label = "‡∏î‡∏µ‡πÄ‡∏¢‡∏µ‡πà‡∏¢‡∏°";
      color = "#10B981";
    } else if (percentage >= 62.5) {
      label = "‡∏î‡∏µ";
      color = "#22C55E";
    } else if (percentage >= 37.5) {
      label = "‡∏õ‡∏≤‡∏ô‡∏Å‡∏•‡∏≤‡∏á";
      color = "#F59E0B";
    } else {
      label = "‡∏ï‡πâ‡∏≠‡∏á‡∏õ‡∏£‡∏±‡∏ö‡∏õ‡∏£‡∏∏‡∏á";
      color = "#EF4444";
    }

    // Get advice for weak categories
    let advice: string | null = null;
    if (percentage < 75) {
      const catAdvice = CATEGORY_ADVICE[cat.id];
      if (catAdvice) {
        advice = percentage < 50 ? catAdvice.low : catAdvice.medium;
      }
    }

    return {
      category: cat,
      score,
      maxScore: cat.maxScore,
      percentage,
      label,
      color,
      advice,
    };
  });
}

export function getQuizResult(totalScore: number): QuizResultLevel {
  return (
    QUIZ_RESULTS.find(
      (r) => totalScore >= r.minScore && totalScore <= r.maxScore
    ) ?? QUIZ_RESULTS[QUIZ_RESULTS.length - 1]
  );
}
